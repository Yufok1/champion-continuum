SYSTEMS = {
    "portable": PORTABLE,
    "codex": CODEX,
    "tiny": TINY,
    "no-tools": NO_TOOLS,
    "relay": RELAY,
}


def list_profiles() -> list[str]:
    return sorted(SYSTEMS)


def get_system_prompt(profile: str = "portable", continuum: Any | None = None) -> str:
    key = str(profile or "portable").strip().lower()
    prompt = SYSTEMS.get(key, PORTABLE)

    if key == "relay" and continuum is not None and hasattr(continuum, "indexed_tool_summary"):
        try:
            summary = continuum.indexed_tool_summary()
        except Exception:
            summary = {"count": 0, "servers": []}
        prompt += (
            "\n\nCONTINUUM NATIVE TOOLS: Built-in Continuum faculties are reachable through "
            "[[tools: search | continuum_<name>]] and usually return native.<tool> calls even "
            "when the MCP sidecar has zero indexed tools. Important native searches include "
            "continuum_state, continuum_settings, continuum_providers, continuum_utility_daemons, "
            "continuum_music_forge_state, continuum_music_compose_packet, "
            "continuum_music_backend_preset, continuum_expressive_wallpaper, "
            "continuum_wallpaper_text, continuum_remember, and continuum_search. "
            "Use native results when present; only report requires_backend for the specific "
            "tool result that says so.\n"
        )
        if summary.get("count"):
            servers = ", ".join(summary.get("servers") or []) or "external"
            prompt += (
                f"\n\nEXTERNAL TOOLS: You have {summary['count']} real tools available "
                f"(servers: {servers}). There are too many to list, so you QUERY for the right one:\n"
                "    [[tools: search | what you need to do]]\n"
                "That returns a few matching tools, each with a ready-to-use call-template. To run one:\n"
                "    [[tool: <server>.<name> | arg1=value, arg2=value]]\n"
                "Results return in the next [[continuum-results]] block; then answer in plain words.\n"
                "When a tool can do what the user asked, search for it and call it.\n"
            )

    return prompt
